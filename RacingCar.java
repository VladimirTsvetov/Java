public class RacingCar {
    private final int speed;
    private final int racingNumber;

    public RacingCar(int speed, int racingNumber) {
        this.speed = speed;
        this.racingNumber = racingNumber;
    }

    public int getSpeed() {
        return speed;
    }

    public int getRacingNumber() {
        return racingNumber;
    }
}
